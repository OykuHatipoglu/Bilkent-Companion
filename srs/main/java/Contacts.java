package com.example.chat;

public class Contacts {
    public String name, id;

    public Contacts(String name, String id)
    {
        this.name = name;
        this.id = id;
    }


    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getId()
    {
        return id;
    }

    public void setStatus(String status)
    {
        this.id = status;
    }

}
